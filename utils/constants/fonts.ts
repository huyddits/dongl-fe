// Helper function to load font CSS dynamically
export const loadFontCSS = (fontValue: string, fontSrc: string) => {
  const styleId = `font-${fontValue}`

  // Check if font is already loaded
  if (document.getElementById(styleId)) {
    return
  }

  // Create CSS for the font
  const css = `
    @font-face {
      font-family: '${fontValue}';
      src: url('${fontSrc}') format('woff2');
      font-weight: normal;
      font-style: normal;
      font-display: swap;
    }
  `

  // Create and append style element
  const style = document.createElement('style')
  style.id = styleId
  style.textContent = css
  document.head.appendChild(style)
}
